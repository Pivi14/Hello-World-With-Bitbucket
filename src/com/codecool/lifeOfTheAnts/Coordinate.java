package com.codecool.lifeOfTheAnts;

public class Coordinate {
    private int xCoordinate;
    private int yCoordinate;

    public Coordinate(int x, int y){
        xCoordinate = x;
        yCoordinate = y;
    }

    public int getxCoordinate() {
        return xCoordinate;
    }

    public int getyCoordinate() {
        return yCoordinate;
    }
}
